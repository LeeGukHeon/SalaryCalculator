/**
 * 간이세액 조회 함수
 * @param {number} monthlyGrossSalary 월급여액 (원)
 * @param {number} dependents 공제대상가족 수 (1~11명, 본인 포함)
 * @returns {number} 간이세액 (원)
 */
export function getSimpleTax(monthlyGrossSalary, dependents = 1) {
  const salaryInThousand = Math.floor(monthlyGrossSalary / 1000);

  // 공제가족 수 범위 체크 (1~11)
  const validDependents = Math.max(1, Math.min(11, dependents));
  const index = validDependents - 1;

  // 구간별 다른 단위 적용
  let roundedSalary;

  if (salaryInThousand < 1500) {
    // 저소득: 5천원 단위로 내림
    roundedSalary = Math.floor(salaryInThousand / 5) * 5;
  } else if (salaryInThousand < 3000) {
    // 중소득: 10천원 단위로 내림
    roundedSalary = Math.floor(salaryInThousand / 10) * 10;
  } else {
    // 고소득: 20천원 단위로 내림
    roundedSalary = Math.floor(salaryInThousand / 20) * 20;
  }

  // 10,000천원 이하: 테이블 조회
  if (roundedSalary <= 10000) {
    const taxArray = SIMPLE_TAX_TABLE_2026[roundedSalary];
    if (taxArray) {
      return taxArray[index];
    }

    // 테이블에 없으면 보간
    return interpolateTax(roundedSalary, index);
  }

  // 10,000천원 초과: 공식 계산
  return calculateHighIncomeTax(salaryInThousand, validDependents);
}
/**
 * 선형 보간 (테이블에 없는 구간용)
 */
function interpolateTax(salary, index) {
  const keys = Object.keys(SIMPLE_TAX_TABLE_2026)
    .map(Number)
    .sort((a, b) => a - b);

  // 가장 가까운 하위/상위 키 찾기
  let lower = 0,
    upper = 0;
  for (let i = 0; i < keys.length - 1; i++) {
    if (keys[i] <= salary && salary < keys[i + 1]) {
      lower = keys[i];
      upper = keys[i + 1];
      break;
    }
  }

  if (lower === 0 || upper === 0) return 0;

  const lowerTax = SIMPLE_TAX_TABLE_2026[lower][index];
  const upperTax = SIMPLE_TAX_TABLE_2026[upper][index];

  // 선형 보간
  const ratio = (salary - lower) / (upper - lower);
  return Math.round(lowerTax + (upperTax - lowerTax) * ratio);
}

/**
 * 10,000천원 초과 구간 소득세 계산
 * (간이세액표 공식 기반)
 */
function calculateHighIncomeTax(salaryThousand, dependents) {
  // 10,000천원의 기준 세액
  const baseTax = SIMPLE_TAX_TABLE_2026[10000][dependents - 1];

  if (salaryThousand <= 14000) {
    // 10,000~14,000천원
    const excess = salaryThousand - 10000;
    return baseTax + Math.round(excess * 0.98 * 0.35) + 25000;
  } else if (salaryThousand <= 28000) {
    // 14,000~28,000천원
    const excess = salaryThousand - 14000;
    return baseTax + 1397000 + Math.round(excess * 0.98 * 0.38);
  } else if (salaryThousand <= 30000) {
    // 28,000~30,000천원
    const excess = salaryThousand - 28000;
    return baseTax + 6610600 + Math.round(excess * 0.98 * 0.4);
  } else if (salaryThousand <= 45000) {
    // 30,000~45,000천원
    const excess = salaryThousand - 30000;
    return baseTax + 7394600 + Math.round(excess * 0.4);
  } else if (salaryThousand <= 87000) {
    // 45,000~87,000천원
    const excess = salaryThousand - 45000;
    return baseTax + 13394600 + Math.round(excess * 0.42);
  } else {
    // 87,000천원 초과
    const excess = salaryThousand - 87000;
    return baseTax + 31034600 + Math.round(excess * 0.45);
  }
}
